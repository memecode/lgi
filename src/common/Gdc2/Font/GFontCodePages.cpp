// Utf-16 conversion
int LgiByteLen(const void *p, const char *cp)
{
	if (p && cp)
	{
		if (stricmp(cp, "utf-16") == 0)
		{
			return StrlenW((char16*)p) * sizeof(char16);
		}
		else
		{
			return strlen((char*)p);
		}
	}

	return 0;
}

