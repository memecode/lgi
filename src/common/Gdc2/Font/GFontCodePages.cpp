char *LgiSeekUtf8(const char *Ptr, int D, char *Start)
{
	uchar *p = (uchar*)Ptr;
	if (p)
	{
		if (D >= 0)
		{
			for (int i=0; i<D; i++)
			{
				LgiNextUtf8((char*&)p);
			}
		}
		else if (Start)
		{
			for (int i=0; i<-D && p>(uchar*)Start; i++)
			{
				p--;
				while (p>(uchar*)Start && IsUtf8_Trail(*p))
					p--;
			}
		}
		else
		{
			// You must pass a start point to move backwards in
			// the utf-8 string, otherwise you can run off the
			// beginning of the array.
			LgiAssert(0);
		}
	}

	return (char*)p;
}

bool LgiMatchCharset(short *Map, char16 *Utf, bool &Has8Bit)
{
	if (Map && Utf)
	{
		// Test Charset because we have a map of all the chars in it...
		char16 *c;
		for (c = Utf; *c; c++)
		{
			if (*c > 0x7f)
			{
				// Check char
				Has8Bit = true;
				int i;
				for (i=0; i<128; i++)
				{
					if (Map[i] == *c)
						break;
				}

				if (i >= 128)
				{
					// Char not found
					return false;
				}
			}
		}
		if (Has8Bit)
		{
			if (!*c)
			{
				return true;
			}
		}
	}

	return false;
}

const char *LgiDetectCharset(const char *Utf8, int Len, List<char> *Prefs)
{
	const char *Status = "utf-8"; // The default..

	GAutoWString Utf((char16*)LgiNewConvertCp("utf-16", Utf8, "utf-8", Len));
	if (Utf)
	{
		if (Prefs)
		{
			for (char *p = Prefs->First(); p; p = Prefs->Next())
			{
				GCharset *Cp = CharsetSystem.GetCsInfo(p);
				if (Cp &&
					stricmp(Cp->Charset, "us-ascii") != 0 &&
					Cp->UnicodeMap)
				{
					bool Has8Bit = false;
					if (LgiMatchCharset(Cp->UnicodeMap, Utf, Has8Bit))
					{
						return Cp->Charset;
					}
					if (!Has8Bit)
					{
						return "us-ascii";
					}
				}
			}
		}

		for (GCharset *Cp = LgiCharsets + 1; Cp->Charset; Cp++)
		{
			if (Cp->UnicodeMap)
			{
				bool Has8Bit = false;
				if (LgiMatchCharset(Cp->UnicodeMap, Utf, Has8Bit))
				{
					return Cp->Charset;
				}
				
				if (!Has8Bit)
				{
					return "us-ascii";
				}
			}
		}
	}

	return Status;
}

char *LgiToNativeCp(const char *In, int InLen)
{
	const char *Cp = LgiAnsiToLgiCp();

	#ifdef WIN32
	GCharset *CpInfo = LgiGetCpInfo(Cp);
	if (!CpInfo || CpInfo->Type == CpWindowsDb)
	{
		if (In)
		{
			// Double byte charset conversion, don't rely on iconv
			// being around to do the conversion.
			setlocale(LC_ALL, ".ACP");

			if (InLen < 0)
			{
				InLen = strlen(In);
			}

			char16 *Wide = LgiNewUtf8To16(In, InLen);
			if (Wide)
			{
				int Len = wcstombs(0, Wide, StrlenW(Wide));
				char *Buf = Len > 0 ? new char[Len+1] : 0;
				if (Buf)
				{
					wcstombs(Buf, Wide, InLen);
					Buf[Len] = 0;
				}
				DeleteArray(Wide);
				return Buf;
			}
		}
		return 0;
	}
	#endif

	return (char*)LgiNewConvertCp(Cp, In, "utf-8", InLen);
}

char *LgiFromNativeCp(const char *In, int InLen)
{
	const char *Cp = LgiAnsiToLgiCp();

	#ifdef WIN32
	GCharset *CpInfo = LgiGetCpInfo(Cp);
	if (!CpInfo || CpInfo->Type == CpWindowsDb)
	{
		if (In)
		{
			// Double byte charset conversion, don't rely on iconv
			// being around to do the conversion.
			setlocale(LC_ALL, ".ACP");

			if (InLen < 0)
			{
				#ifdef __GNUC__
					// FIXME
					InLen = strlen(In);
				#else
					InLen = _mbstrlen(In);
				#endif
			}
			else
			{
				// Work out how many chars 'InLen' bytes is
				int Bytes = InLen;
				const char *i = In;
				int Chars = 0;
				while (*i && Bytes > 0)
				{
					int n = mblen(i, MB_CUR_MAX);
					if (n > 0)
					{
						Chars++;
						Bytes -= n;
						i += n;
					}
					else break;
				}
				InLen = Chars;

			}

			int Len = mbstowcs(0, In, InLen);
			if (Len)
			{
				char16 *Buf = new char16[Len+1];
				if (Buf)
				{
					mbstowcs(Buf, In, InLen);
					Buf[Len] = 0;
					char *Utf8 = LgiNewUtf16To8(Buf);
					DeleteArray(Buf);
					return Utf8;
				}
			}
		}

		return 0;
	}
	#endif

	return (char*)LgiNewConvertCp("utf-8", In, Cp, InLen);
}

