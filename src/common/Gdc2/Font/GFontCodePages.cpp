bool LgiIsUtf8(const char *s, int len)
{
	#define LenCheck(Need) \
		if (len >= 0 && (len - (s - Start)) < Need) \
			goto Utf8Error;
	#define TrailCheck() \
		if (!IsUtf8_Trail(*s)) \
			goto Utf8Error; \
		s++;

	if (!s || *s == 0)
		return true;

	const char *Start = s;
	while (*s && (len < 0 || ((s - Start) < len) ) )
	{
		if (IsUtf8_1Byte(*s))
		{
			s++;
		}
		else if (IsUtf8_2Byte(*s))
		{
			s++;
			LenCheck(1);
			TrailCheck();
		}
		else if (IsUtf8_3Byte(*s))
		{
			s++;
			LenCheck(2);
			TrailCheck();
			TrailCheck();
		}
		else if (IsUtf8_4Byte(*s))
		{
			s++;
			LenCheck(3);
			TrailCheck();
			TrailCheck();
			TrailCheck();
		}
		else goto Utf8Error;
	}
	return true;

Utf8Error:
	LgiTrace("%s:%i - Invalid utf...\n", _FL);
	return false;
}
				
