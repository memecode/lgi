class LgiIso2022Jp
{
public:
	LgiIso2022Jp()
	{
		int n, o = 0, i = 0;

		for (n=0; n<3; n++) iso2022jp_map[o++] = &iso2022jp_blocks[i++];
		o += 13;
		for (n=0; n<4; n++) iso2022jp_map[o++] = &iso2022jp_blocks[i++];;
		o += 4;
		iso2022jp_map[o++] = &iso2022jp_blocks[i++];;
		o += 14;
		for (n=0; n<41; n++) iso2022jp_map[o++] = &iso2022jp_blocks[i++];;
		o += 47;
		iso2022jp_map[o++] = &iso2022jp_blocks[i++];;

		LgiAssert(o == 128);
	}

	bool CanEncode(char16 *s, int l)
	{
		if (s)
		{
			if (l < 0) l = StrlenW(s);
			for (int i=0; i<l; i++)
			{
				int u = s[i];
				iso2022jp_block *block = iso2022jp_map[u >> 9];
				if (!*block)
				{
					return false;
				}
				u &= 0x1ff;
				if (
						(
							*block[u >> 5]
							&
							(1 << (u & 0x1f))
						)
						== 0
					)
				{
					return false;
				}
			}

			return true;
		}

		return false;
	}
} Iso2022Jp;

